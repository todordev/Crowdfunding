// no direct access
defined('_JEXEC') or die;

class CrowdfundingViewComment extends JViewLegacy
{
    /**
     * @var JDocumentHtml
     */
    public $document;

    protected $state;
    protected $item;
    protected $form;

    protected $documentTitle;
    protected $option;

    public function display($tpl = null)
    {
        $this->option = JFactory::getApplication()->input->get('option');
        
        $this->state = $this->get('State');
        $this->item  = $this->get('Item');
        $this->form  = $this->get('Form');

        // Prepare actions, behaviors, scripts and document
        $this->addToolbar();
        $this->setDocument();

        parent::display($tpl);
    }

    protected function addToolbar()
    {
        JFactory::getApplication()->input->set('hidemainmenu', true);
        $isNew = ((int)$this->item->id === 0);

        $this->documentTitle = $isNew ? JText::_('COM_CROWDFUNDING_NEW_COMMENT') : JText::_('COM_CROWDFUNDING_EDIT_COMMENT');

        JToolbarHelper::title($this->documentTitle);

        JToolbarHelper::apply('comment.apply');
        JToolbarHelper::save('comment.save');

        if (!$isNew) {
            JToolbarHelper::cancel('comment.cancel');
        } else {
            JToolbarHelper::cancel('comment.cancel', 'JTOOLBAR_CLOSE');
        }
    }


    protected function setDocument()
    {
        $this->document->setTitle($this->documentTitle);

        // Scripts
        JHtml::_('behavior.formvalidation');
        JHtml::_('behavior.tooltip');

        JHtml::_('formbehavior.chosen', 'select');

        $this->document->addScript('../media/' . $this->option . '/js/admin/' . strtolower($this->getName()) . '.js');
    }
}
